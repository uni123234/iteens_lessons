try:
    # Блок, в якому розміщений підозрілий код
    num1 = int(input("Введіть перше число: "))
    num2 = int(input("Введіть друге число: "))
    result = num1 / num2
    print("Результат: ", result)
except ValueError:
    # Обробка помилки, якщо введені дані не можуть бути конвертовані у ціле число
    print("Помилка: Введені дані не є числами")
except ZeroDivisionError:
    # Обробка помилки, якщо введено число 0 як знаменник
    print("Помилка: Ділення на нуль неможливе")
except Exception as e:
    # Обробка будь-яких інших винятків, які не були включені до попередніх блоків except
    print("Сталася помилка:", str(e))
