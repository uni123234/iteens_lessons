import sqlite3

try:
    conn = sqlite3.connect("database.db")  # Встановлення з'єднання з базою даних
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM users")  # Виконання запиту до бази даних
    rows = cursor.fetchall()  # Отримання результатів запиту
    for row in rows:
        print(row)
    conn.close()  # Закриття з'єднання з базою даних
except sqlite3.DatabaseError:
    print("Помилка: Помилка бази даних")
except Exception as e:
    print("Сталася помилка:", str(e))

